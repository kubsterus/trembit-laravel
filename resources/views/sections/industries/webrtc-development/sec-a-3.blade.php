                            <h3>High level of security</h3>
                            <p>By choosing this system, you can be completely sure of the security of your data. Encryption is mandatory for all WebRTC components. Since it is not a plugin, it runs inside the browser without creating a new process, so that no malware can enter the user's system.</p>
                        </div>
                        <div class="item-ap-k">
                            <picture><source srcset="../view/images/industries/speed.webp" type="image/webp"><img src="../view/images/industries/speed.png" alt="Speed"></source></picture>
                            <h3>Speed Boost</h3>
                            <p>No more routing through the server, which reduces latency and bandwidth consumption. Direct communication increases the speed of data transfer and file sharing.</p>
