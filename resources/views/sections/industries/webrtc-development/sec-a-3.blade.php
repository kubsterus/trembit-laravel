                        <h2>The Advantages Of Webrtc</h2>
                        <div class="item-ap-k">
                            <picture><source srcset="../view/images/industries/group1234.webp" type="image/webp"><img src="../view/images/industries/group1234.png" alt="Group"></source></picture>
                            <h3>No third party app needed</h3>
                            <p>While most systems need software installation, WebRTC does not, which is very convenient and time saving.</p>
                        </div>
                        <div class="item-ap-k">
                            <picture><source srcset="../view/images/industries/network_1.webp" type="image/webp"><img src="../view/images/industries/network_1.png" alt="Network"></source></picture>
                            <h3>High quality connection</h3>
                            <p>State-of-the-art audio and video equipment with features such as echo and noise cancellation ensure excellent call quality. The smart technology adapts to your Internet connection and adjusts communication quality, so even if the connection is poor, you will still receive high-quality video and audio signals. Due to the automatic microphone sensitivity, the sound will be adjusted as accurately as possible so that the interlocutors can comfortably hear each other.</p>
                        </div>
                        <div class="item-ap-k">
